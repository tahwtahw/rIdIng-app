class _RoadTownshipPickerView extends StatefulWidget {
  final String roadName;
  final List<_Township> townships;
  final List<({double lat, double lng})> roadPts;
  final double defaultSpeed;
  final bool isFavorite;
  final VoidCallback onToggleFavorite;
  final void Function(
      _Township start, _Township end, DateTime departure, double speed) onAnalyze;
  const _RoadTownshipPickerView({
    required this.roadName, required this.townships, required this.roadPts,
    required this.defaultSpeed, required this.onAnalyze,
    this.isFavorite = false, required this.onToggleFavorite,
  });
  @override State<_RoadTownshipPickerView> createState() => _RoadTownshipPickerViewState();
}

class _RoadTownshipPickerViewState extends State<_RoadTownshipPickerView> {
  final _searchCtrl = TextEditingController();
  final _speedCtrl  = TextEditingController();
  DateTime _departure = DateTime.now();
  String? _speedError;
  String _filter = '';

  // 選定的起終點 township
  _Township? _startTownship;
  _Township? _endTownship;

  List<_Township> get _filtered {
    if (_filter.isEmpty) return widget.townships;
    return widget.townships.where((t) =>
        t.name.contains(_filter) || t.county.contains(_filter)).toList();
  }

  @override
  void initState() {
    super.initState();
    _speedCtrl.text = widget.defaultSpeed.toStringAsFixed(0);
    if (widget.townships.length >= 2) {
      _startTownship = widget.townships.first;
      _endTownship   = widget.townships.last;
    }
  }

  @override
  void dispose() {
    _searchCtrl.dispose(); _speedCtrl.dispose(); super.dispose();
  }

  double? get _parsedSpeed {
    final v = double.tryParse(_speedCtrl.text.trim());
    if (v == null || v <= 0 || v > 100) return null;
    return v;
  }

  void _submit() {
    if (_startTownship == null || _endTownship == null) return;
    final speed = _parsedSpeed;
    if (speed == null) {
      setState(() => _speedError = '請輸入 1～100');
      return;
    }
    widget.onAnalyze(_startTownship!, _endTownship!, _departure, speed);
  }

  @override
  Widget build(BuildContext context) {
    final cs = Theme.of(context).colorScheme;
    final filtered = _filtered;
    final isAllTownships = widget.townships.length > 100; // 全台模式

    return Column(children: [
      // ── 頂部固定區 ─────────────────────────────────────────
      Container(
        color: cs.surface,
        padding: const EdgeInsets.fromLTRB(14, 12, 14, 8),
        child: Column(crossAxisAlignment: CrossAxisAlignment.start, children: [
          Row(children: [
            const Icon(Icons.route, size: 18),
            const SizedBox(width: 8),
            Expanded(child: Text(widget.roadName,
                style: const TextStyle(fontSize: 15, fontWeight: FontWeight.bold))),
            IconButton(
              icon: Icon(widget.isFavorite ? Icons.star : Icons.star_border,
                  color: widget.isFavorite ? Colors.amber : null, size: 20),
              tooltip: widget.isFavorite ? '移除常用' : '加入常用路段',
              padding: EdgeInsets.zero,
              constraints: const BoxConstraints(),
              onPressed: widget.onToggleFavorite,
            ),
          ]),
          const SizedBox(height: 4),
          Text(
            isAllTownships
                ? '未能自動識別路段，請從下方搜尋並選擇起點與終點'
                : '找到 ${widget.townships.length} 個途經地區，點選選擇起終點',
            style: TextStyle(fontSize: 11, color: cs.outline),
          ),
          const SizedBox(height: 8),

          // 搜尋框
          TextField(
            controller: _searchCtrl,
            decoration: InputDecoration(
              hintText: '搜尋鄉鎮區…',
              hintStyle: const TextStyle(fontSize: 13),
              isDense: true,
              contentPadding: const EdgeInsets.symmetric(horizontal: 10, vertical: 8),
              prefixIcon: const Icon(Icons.search, size: 18),
              border: OutlineInputBorder(borderRadius: BorderRadius.circular(8)),
              filled: true,
              fillColor: cs.surfaceVariant,
            ),
            onChanged: (v) => setState(() => _filter = v.trim()),
          ),
          const SizedBox(height: 8),

          // 已選起終點顯示
          Row(children: [
            _SelectedChip(
              label: _startTownship != null
                  ? '起：${_startTownship!.name}'
                  : '點選起點',
              color: Colors.green,
              onClear: () => setState(() => _startTownship = null),
            ),
            const SizedBox(width: 8),
            const Icon(Icons.arrow_forward, size: 16, color: Colors.grey),
            const SizedBox(width: 8),
            _SelectedChip(
              label: _endTownship != null
                  ? '終：${_endTownship!.name}'
                  : '點選終點',
              color: Colors.red,
              onClear: () => setState(() => _endTownship = null),
            ),
          ]),
        ]),
      ),
      const Divider(height: 1),

      // ── 鄉鎮列表 ───────────────────────────────────────────
      Expanded(
        child: ListView.builder(
          padding: const EdgeInsets.symmetric(horizontal: 12, vertical: 4),
          itemCount: filtered.length,
          itemBuilder: (ctx, i) {
            final t = filtered[i];
            final isStart = _startTownship?.name == t.name &&
                            _startTownship?.county == t.county;
            final isEnd   = _endTownship?.name == t.name &&
                            _endTownship?.county == t.county;
            Color? tileColor;
            if (isStart) tileColor = Colors.green.withOpacity(0.12);
            if (isEnd)   tileColor = Colors.red.withOpacity(0.12);

            return ListTile(
              dense: true,
              tileColor: tileColor,
              shape: RoundedRectangleBorder(borderRadius: BorderRadius.circular(8)),
              leading: isStart
                  ? const Icon(Icons.trip_origin, color: Colors.green, size: 18)
                  : isEnd
                      ? const Icon(Icons.place, color: Colors.red, size: 18)
                      : Icon(Icons.location_on_outlined, size: 18,
                            color: cs.outline),
              title: Text(t.name,
                  style: TextStyle(
                    fontSize: 13,
                    fontWeight: (isStart || isEnd)
                        ? FontWeight.bold : FontWeight.normal,
                  )),
              subtitle: Text(t.county,
                  style: TextStyle(fontSize: 11, color: cs.outline)),
              trailing: isStart
                  ? const Text('起點',
                      style: TextStyle(color: Colors.green, fontSize: 11))
                  : isEnd
                      ? const Text('終點',
                          style: TextStyle(color: Colors.red, fontSize: 11))
                      : null,
              onTap: () => setState(() {
                if (_startTownship == null) {
                  _startTownship = t;
                } else if (_endTownship == null) {
                  if (t.name != _startTownship!.name) _endTownship = t;
                } else {
                  // 重置，從起點開始
                  _startTownship = t;
                  _endTownship = null;
                }
              }),
            );
          },
        ),
      ),

      // ── 底部固定：時間、速度、分析 ─────────────────────────
      Container(
        color: cs.surface,
        padding: EdgeInsets.fromLTRB(14, 8, 14,
            14 + MediaQuery.of(context).viewInsets.bottom),
        child: Column(mainAxisSize: MainAxisSize.min, children: [
          const Divider(height: 12),
          Row(children: [
            const Icon(Icons.schedule, size: 16),
            const SizedBox(width: 6),
            const Text('出發', style: TextStyle(fontSize: 13)),
            const Spacer(),
            OutlinedButton(
              style: OutlinedButton.styleFrom(
                  padding: const EdgeInsets.symmetric(horizontal: 12, vertical: 6)),
              onPressed: () async {
                final now = DateTime.now();
                final picked = await showTimePicker(
                  context: context,
                  initialTime: TimeOfDay.fromDateTime(_departure),
                );
                if (picked != null) setState(() => _departure = DateTime(
                    now.year, now.month, now.day, picked.hour, picked.minute));
              },
              child: Text(_fmtTime(_departure),
                  style: const TextStyle(
                      fontSize: 15, fontWeight: FontWeight.bold)),
            ),
            const SizedBox(width: 16),
            const Icon(Icons.speed, size: 16),
            const SizedBox(width: 6),
            SizedBox(
              width: 90,
              child: TextField(
                controller: _speedCtrl,
                keyboardType: const TextInputType.numberWithOptions(decimal: true),
                textAlign: TextAlign.center,
                style: const TextStyle(fontSize: 14, fontWeight: FontWeight.bold),
                decoration: InputDecoration(
                  suffixText: 'km/h',
                  errorText: _speedError,
                  isDense: true,
                  contentPadding: const EdgeInsets.symmetric(
                      horizontal: 6, vertical: 8),
                  border: const OutlineInputBorder(),
                ),
                onChanged: (_) => setState(() => _speedError = null),
              ),
            ),
          ]),
          const SizedBox(height: 8),
          FilledButton.icon(
            onPressed: (_startTownship != null && _endTownship != null &&
                        _startTownship != _endTownship)
                ? _submit : null,
            icon: const Icon(Icons.analytics),
            label: Text(
              (_startTownship != null && _endTownship != null)
                  ? '分析 ${_startTownship!.name} → ${_endTownship!.name} 天氣'
                  : '請先選擇起點與終點',
              style: const TextStyle(fontSize: 13),
            ),
            style: FilledButton.styleFrom(
                minimumSize: const Size.fromHeight(46)),
          ),
        ]),
      ),
    ]);
  }
}

