class _ManualRouteSheet extends StatefulWidget {
  final String roadName;
  final void Function(
      _Station start, _Station end, DateTime departure, double speed) onAnalyze;
  const _ManualRouteSheet({required this.roadName, required this.onAnalyze});
  @override
  State<_ManualRouteSheet> createState() => _ManualRouteSheetState();
}

class _ManualRouteSheetState extends State<_ManualRouteSheet> {
  final _startCtrl = TextEditingController();
  final _endCtrl   = TextEditingController();
  final _speedCtrl = TextEditingController(text: '15');
  DateTime _departure = DateTime.now();
  bool _loading = false;
  String? _error;
  String? _speedError;

  @override
  void dispose() {
    _startCtrl.dispose(); _endCtrl.dispose(); _speedCtrl.dispose();
    super.dispose();
  }

  Future<void> _submit() async {
    final startKw = _startCtrl.text.trim();
    final endKw   = _endCtrl.text.trim();
    if (startKw.isEmpty || endKw.isEmpty) {
      setState(() => _error = '請輸入起點與終點'); return;
    }
    final speed = double.tryParse(_speedCtrl.text.trim());
    if (speed == null || speed <= 0 || speed > 100) {
      setState(() => _speedError = '請輸入 1～100'); return;
    }
    setState(() { _loading = true; _error = null; _speedError = null; });
    final startGeo = await _geocode(startKw);
    final endGeo   = await _geocode(endKw);
    if (!mounted) return;
    if (startGeo == null || endGeo == null) {
      setState(() { _loading = false; _error = '無法找到起點或終點，請換個寫法再試'; }); return;
    }
    setState(() => _loading = false);
    widget.onAnalyze(
      _Station(startGeo.name, startGeo.lat, startGeo.lng),
      _Station(endGeo.name,   endGeo.lat,   endGeo.lng),
      _departure, speed,
    );
  }

  @override
  Widget build(BuildContext context) {
    final cs = Theme.of(context).colorScheme;
    return Padding(
      padding: EdgeInsets.only(bottom: MediaQuery.of(context).viewInsets.bottom),
      child: SingleChildScrollView(child: Column(mainAxisSize: MainAxisSize.min, children: [
        const SizedBox(height: 8),
        Container(width: 36, height: 4,
            decoration: BoxDecoration(color: Colors.grey.shade400,
                borderRadius: BorderRadius.circular(2))),
        Padding(
          padding: const EdgeInsets.fromLTRB(16, 12, 16, 4),
          child: Column(crossAxisAlignment: CrossAxisAlignment.start, children: [
            Text('手動設定路線：${widget.roadName}',
                style: const TextStyle(fontWeight: FontWeight.bold, fontSize: 15)),
            const SizedBox(height: 4),
            Text('輸入起點與終點地名，系統將自動查詢座標並分析沿途天氣',
                style: TextStyle(fontSize: 12, color: cs.outline)),
          ]),
        ),
        const Divider(),
        if (_error != null)
          Padding(padding: const EdgeInsets.fromLTRB(16, 8, 16, 0),
              child: Text(_error!, style: TextStyle(color: cs.error, fontSize: 13))),
        Padding(
          padding: const EdgeInsets.fromLTRB(16, 12, 16, 8),
          child: TextField(controller: _startCtrl,
            decoration: InputDecoration(
              labelText: '起點', hintText: '例如：台南市安平區',
              prefixIcon: const Icon(Icons.trip_origin, color: Colors.green),
              border: const OutlineInputBorder(), isDense: true)),
        ),
        Padding(
          padding: const EdgeInsets.fromLTRB(16, 0, 16, 8),
          child: TextField(controller: _endCtrl,
            decoration: InputDecoration(
              labelText: '終點', hintText: '例如：高雄市左營區',
              prefixIcon: const Icon(Icons.place, color: Colors.red),
              border: const OutlineInputBorder(), isDense: true)),
        ),
        const Divider(),
        Padding(
          padding: const EdgeInsets.symmetric(horizontal: 16, vertical: 8),
          child: Row(children: [
            const Icon(Icons.schedule, size: 18), const SizedBox(width: 8),
            const Text('出發時間', style: TextStyle(fontWeight: FontWeight.w600)),
            const Spacer(),
            OutlinedButton(
              onPressed: () async {
                final now = DateTime.now();
                final picked = await showTimePicker(context: context,
                    initialTime: TimeOfDay.fromDateTime(_departure));
                if (picked != null) setState(() => _departure = DateTime(
                    now.year, now.month, now.day, picked.hour, picked.minute));
              },
              child: Text(_fmtTime(_departure),
                  style: const TextStyle(fontSize: 17, fontWeight: FontWeight.bold)),
            ),
          ]),
        ),
        Padding(
          padding: const EdgeInsets.fromLTRB(16, 0, 16, 8),
          child: Row(children: [
            const Icon(Icons.speed, size: 18), const SizedBox(width: 8),
            const Text('平均速度', style: TextStyle(fontWeight: FontWeight.w600)),
            const Spacer(),
            SizedBox(width: 110,
              child: TextField(
                controller: _speedCtrl,
                keyboardType: const TextInputType.numberWithOptions(decimal: true),
                textAlign: TextAlign.center,
                style: const TextStyle(fontSize: 17, fontWeight: FontWeight.bold),
                decoration: InputDecoration(
                  suffixText: 'km/h', errorText: _speedError, isDense: true,
                  contentPadding: const EdgeInsets.symmetric(horizontal: 8, vertical: 10),
                  border: const OutlineInputBorder()),
                onChanged: (_) => setState(() => _speedError = null),
              )),
          ]),
        ),
        const Divider(),
        Padding(
          padding: const EdgeInsets.fromLTRB(16, 8, 16, 16),
          child: _loading
              ? const Center(child: CircularProgressIndicator())
              : FilledButton.icon(
                  onPressed: _submit,
                  icon: const Icon(Icons.analytics),
                  label: const Text('開始分析路線天氣'),
                  style: FilledButton.styleFrom(minimumSize: const Size.fromHeight(46))),
        ),
      ])),
    );
  }
