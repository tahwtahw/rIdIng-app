  List<String> _favoriteRoads = [];  // 用戶常用路段
  _RoadState     _roadState = _RoadState.idle;
  String?        _errorMsg;
  _PointWeather? _pointWeather;
  List<_RoutePoint>? _routePoints;
  String?        _routeTitle;
  String?        _foundRoadName;
  List<_Station>? _foundRoadStations;
  List<({double lat, double lng})>? _foundRoadPts;
