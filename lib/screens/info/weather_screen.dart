                            expand: false,
                            initialChildSize: 0.7,
                            maxChildSize: 0.95,
                            builder: (_, ctrl) => _TownDetailSheet(
                                pw: pw, scrollCtrl: ctrl),
                          ),
                        );
                      }
                    },
                  );
                }).toList(),
              ),
            ),

          const SizedBox(height: 4),
        ],
      ],
    );
  }

  // ── Tab2 主體 ──────────────────────────────────────────────
  Widget _buildRoadBody() {
    switch (_roadState) {
      case _RoadState.loading:
        return const Center(child: Column(
          mainAxisSize: MainAxisSize.min,
          children: [
            CircularProgressIndicator(),
            SizedBox(height: 12),
            Text('正在分析天氣資料…'),
          ],
        ));
      case _RoadState.error:
        return Center(child: Padding(
          padding: const EdgeInsets.all(24),
          child: Column(mainAxisSize: MainAxisSize.min, children: [
            const Icon(Icons.cloud_off, size: 48, color: Colors.grey),
            const SizedBox(height: 12),
            Text(_errorMsg ?? '發生錯誤', textAlign: TextAlign.center,
                style: const TextStyle(color: Colors.grey)),
            // 若來自路段搜尋，提供手動設定路線的備用入口
            if (_foundRoadName != null) ...[
              const SizedBox(height: 20),
              OutlinedButton.icon(
                icon: const Icon(Icons.edit_road),
                label: Text('手動設定「$_foundRoadName」路線分析'),
                onPressed: () => _showManualRouteSetup(_foundRoadName!),
              ),
            ],
          ]),
        ));
      case _RoadState.point:
        return _PointWeatherView(
          data: _pointWeather!,
          roadName: _foundRoadName,
          onBackToRoute: _foundRoadStations != null
              ? () => setState(() => _roadState = _RoadState.roadPicker)
              : null,
          onAnalyzeRoute: _foundRoadStations != null
              ? () => _showRouteSetup(_foundRoadName!, _foundRoadStations!)
              : null,
          onManualRoute: (_foundRoadName != null && _foundRoadStations == null)
              ? () => _showManualRouteSetup(_foundRoadName!)
              : null,
        );
      case _RoadState.townshipPicker:
        return _RoadTownshipPickerView(
          roadName: _foundRoadName!,
          townships: _foundRoadTownships!,
          roadPts: _foundRoadPts!,
          defaultSpeed: _kRoadSpeed[_foundRoadName] ?? 15.0,
          isFavorite: _favoriteRoads.contains(_foundRoadName),
          onToggleFavorite: () => _favoriteRoads.contains(_foundRoadName!)
              ? _removeFavorite(_foundRoadName!)
              : _addFavorite(_foundRoadName!),
          onAnalyze: (start, end, dep, spd) =>
              _doRouteAnalysis(_foundRoadName!,
                  [start.toStation(), end.toStation()], 0, 1, dep, spd),
        );
      case _RoadState.roadPicker:
        return _RoadPickerView(
          roadName: _foundRoadName!,
          stations: _foundRoadStations!,
          onTapStation: _doCoordSearch,
          onAnalyze: () => _showRouteSetup(
              _foundRoadName!, _foundRoadStations!),
        );
      case _RoadState.route:
        return _RouteWeatherView(
            title: _routeTitle!, points: _routePoints!);
      case _RoadState.idle:
      default:
        return _PlaceholderWidget();
    }
  }
