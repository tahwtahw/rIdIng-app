    return ListView(
      padding: const EdgeInsets.symmetric(vertical: 8, horizontal: 12),
      children: [
        // 進度提示
        if (_townLoadingAll)
          Padding(
            padding: const EdgeInsets.only(bottom: 8),
            child: LinearProgressIndicator(
              value: totalTownships > 0 ? loaded / totalTownships : null,
              borderRadius: BorderRadius.circular(4),
            ),
          ),

        for (final region in _kRegions) ...[
          // 區域標題（可摺疊）
          InkWell(
            onTap: () => setState(() {
              if (_expandedRegions.contains(region.name)) {
                _expandedRegions.remove(region.name);
              } else {
                _expandedRegions.add(region.name);
              }
            }),
            borderRadius: BorderRadius.circular(8),
            child: Padding(
              padding: const EdgeInsets.symmetric(vertical: 8),
              child: Row(children: [
                Container(
                  width: 3, height: 18,
                  decoration: BoxDecoration(
                    color: Theme.of(context).colorScheme.primary,
                    borderRadius: BorderRadius.circular(2),
                  ),
                  margin: const EdgeInsets.only(right: 8),
                ),
                Text(region.name,
                    style: const TextStyle(
                        fontWeight: FontWeight.bold, fontSize: 15)),
                const Spacer(),
                Icon(
                  _expandedRegions.contains(region.name)
                      ? Icons.expand_less
                      : Icons.expand_more,
                  color: Theme.of(context).colorScheme.outline,
                ),
              ]),
            ),
          ),

          if (_expandedRegions.contains(region.name))
            SingleChildScrollView(
                    scrollDirection: Axis.horizontal,
              child: Row(
                children: region.townships.map((t) {
                  final pw = _townWeatherMap[t.name];
                  return _TownshipCard(
                    name: t.name,
                    weather: pw,
                    dayIndex: _selectedDay,
                    onTap: () {
                      if (pw != null) {
                        showModalBottomSheet(
                          context: context,
                          isScrollControlled: true,
                          shape: const RoundedRectangleBorder(
                              borderRadius: BorderRadius.vertical(
                                  top: Radius.circular(20))),
