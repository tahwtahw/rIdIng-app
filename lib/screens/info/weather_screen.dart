/// 將輸入轉為顯示用路段名稱（如 "12甲" → "台12甲線"）
String _roadDisplayName(String q) {
  var s = q.trim().replaceAll('臺', '台');
  if (!s.startsWith('台') && RegExp(r'^\d').hasMatch(s)) s = '台$s';
  if (!s.endsWith('線') && !s.endsWith('路') && !s.endsWith('道')) s = '${s}線';
  return s;
}

bool _looksLikeRoad(String q) {
  final s = q.trim();
  // 純數字（含地支）或台/縣/鄉道格式
  if (RegExp(r'^\d+[甲乙丙丁戊己庚]?[線]?$').hasMatch(s)) return true;
  return RegExp(
    r'台\d+[甲乙丙丁戊己庚]?[線]?|縣道\s*\d+|鄉道|省道|快速道路|'
    r'[Hh]ighway|[Rr]oute\s*\d+',
  ).hasMatch(s);
}

Future<({String name, List<({double lat, double lng})> pts})?> _fetchRoadFromOSM(
    String query) async {
  const bbox = '21.8,119.9,25.4,122.2';
  final q = query.trim();
  final full     = q;
  // noSuffix：只去掉末尾的「線/道/路」，保留甲/乙/丙，e.g. 台7甲線→台7甲
  final noSuffix = q.replaceAll(RegExp(r'[線道路]$'), '').trim();
  // noSuffixBase：再去掉甲/乙/丙，e.g. 台7甲→台7（作為廣義 ref 備用）
  final noSuffixBase = noSuffix.replaceAll(RegExp(r'[甲乙丙]$'), '').trim();
  final numOnly  = q.replaceAll(RegExp(r'[^0-9]'), '');               // e.g. 7
  final twRef    = numOnly.isNotEmpty ? '台$numOnly' : '';             // e.g. 台7
  // 甲乙丙後綴（若有）
  final branchSuffix = RegExp(r'[甲乙丙]').firstMatch(noSuffix)?.group(0) ?? '';

  final overpassCandidates = <String>[
    // 1. route relation（最完整）
    '''[out:json][timeout:30];
relation["name"~"$full"]["type"="route"]($bbox);
way(r);out center 500;''',

    // 2. way name 精確比對（含甲乙丙）
    '''[out:json][timeout:30];
(way["name"~"$noSuffix"]["highway"]($bbox););
out center 500;''',

    // 3. ref 比對（台灣省道：台7甲、台7 等格式）
    if (noSuffix.isNotEmpty) '''[out:json][timeout:30];
(way["ref"="$noSuffix"]["highway"]($bbox);
 way["ref:zh"="$noSuffix"]["highway"]($bbox);
 ${branchSuffix.isNotEmpty ? 'way["ref"="$twRef$branchSuffix"]["highway"]($bbox);' : ''}
 way["ref"="$twRef"]["highway"]($bbox);
 way["ref:zh"="$twRef"]["highway"]($bbox););
out center 500;''',

    // 4. 純數字 ref（縣道）
    if (numOnly.isNotEmpty) '''[out:json][timeout:30];
(way["ref"="$numOnly"]["highway"~"primary|trunk|secondary|tertiary|motorway|road"]($bbox););
out center 500;''',

    // 5. 廣義 name 比對（含數字）
    if (numOnly.isNotEmpty) '''[out:json][timeout:30];
(way["name"~"$noSuffixBase"]["highway"~"primary|trunk|secondary|tertiary|road"]($bbox););
out center 500;''',
  ];

  List? elements;
  for (final oq in overpassCandidates) {
    try {
      final res = await http.get(
        Uri.parse('https://overpass-api.de/api/interpreter'
            '?data=${Uri.encodeComponent(oq)}'),
      ).timeout(const Duration(seconds: 30));
      if (res.statusCode != 200) continue;
      final elems = (jsonDecode(res.body)['elements'] as List?)
          ?.where((e) => e['center'] != null)
          .toList();
      if (elems != null && elems.length >= 2) {
        elements = elems;
        break;
      }
    } catch (e) { debugPrint('錯誤：$e'); continue; }
  }

  // Overpass 全部失敗 → 嘗試 Nominatim 搜尋路段多個結果作為替代座標
  if (elements == null || elements.isEmpty) {
    try {
      final r = await http.get(
        Uri.parse('https://nominatim.openstreetmap.org/search'
            '?q=${Uri.encodeComponent(q)}'
            '&format=json&countrycodes=tw&limit=10&accept-language=zh-TW'),
        headers: const {'User-Agent': 'rIdIngApp/1.0 (cycling)'},
      ).timeout(const Duration(seconds: 10));
      if (r.statusCode == 200) {
        final list = jsonDecode(r.body) as List;
        if (list.length >= 2) {
          // 轉成 fake elements 格式
          elements = list.map((e) => {
            'center': {
              'lat': double.parse(e['lat'] as String),
              'lon': double.parse(e['lon'] as String),
            }
          }).toList();
        }
      }
    } catch (e) { debugPrint('錯誤：$e'); }
  }

  if (elements == null || elements.isEmpty) return null;

  var pts = elements.map((e) => (
    lat: (e['center']['lat'] as num).toDouble(),
    lng: (e['center']['lon'] as num).toDouble(),
  )).toList();

  // 依主方向排序
  final latSpan = pts.map((p) => p.lat).reduce(max) - pts.map((p) => p.lat).reduce(min);
  final lngSpan = pts.map((p) => p.lng).reduce(max) - pts.map((p) => p.lng).reduce(min);
  pts.sort(latSpan >= lngSpan
      ? (a, b) => b.lat.compareTo(a.lat)
      : (a, b) => a.lng.compareTo(b.lng));

  // 動態去重：從 1 km 開始，若留存點 < 5 則逐步縮小閾值
  List<({double lat, double lng})> deduped;
  double threshold = 1.0;
  while (true) {
    deduped = [pts.first];
    for (final p in pts.skip(1)) {
      if (_haversineKm(deduped.last.lat, deduped.last.lng, p.lat, p.lng) > threshold) {
        deduped.add(p);
      }
    }
    if (deduped.length >= 5 || threshold < 0.15) break;
    threshold /= 2;
  }
  pts = deduped;

  // 均勻取樣（最多 30 個，作為地理比對用）
  const n = 30;
  final sampled = pts.length <= n
      ? pts
      : List.generate(n, (i) => pts[(i * (pts.length - 1) ~/ (n - 1))]);

  return (name: q, pts: sampled);
}

Future<({double lat, double lng, String name})?> _geocode(String query) async {
  final omTries = [query, '$query 台灣',
    query.replaceAll(RegExp(r'[市縣鄉鎮區]$'), ' 台灣')];
  for (final q in omTries) {
    try {
      final res = await http.get(Uri.parse(
        'https://geocoding-api.open-meteo.com/v1/search'
        '?name=${Uri.encodeComponent(q)}&count=5&language=zh&format=json',
      )).timeout(const Duration(seconds: 7));
      if (res.statusCode != 200) continue;
      final results = jsonDecode(res.body)['results'];
      if (results == null || (results as List).isEmpty) continue;
      final tw = results.firstWhere(
        (r) => (r['country_code'] ?? '') == 'TW',
        orElse: () => null,
      );
      if (tw == null) continue;
      return (
        lat:  (tw['latitude']  as num).toDouble(),
        lng:  (tw['longitude'] as num).toDouble(),
        name: (tw['name'] ?? query) as String,
      );
    } catch (e) { debugPrint('錯誤：$e'); continue; }
  }

  final nomTries = ['$query 台灣', '$query Taiwan', query];
  for (final q in nomTries) {
    try {
      final res = await http.get(
        Uri.parse(
          'https://nominatim.openstreetmap.org/search'
          '?q=${Uri.encodeComponent(q)}'
          '&format=json&countrycodes=tw&limit=3&accept-language=zh-TW',
        ),
        headers: const {'User-Agent': 'rIdIngApp/1.0 (cycling)'},
      ).timeout(const Duration(seconds: 9));
      if (res.statusCode != 200) continue;
      final results = jsonDecode(res.body) as List;
      if (results.isEmpty) continue;
      final r = results[0];
      final displayName = (r['display_name'] as String).split(',')[0].trim();
      return (
        lat:  double.parse(r['lat'] as String),
        lng:  double.parse(r['lon'] as String),
        name: displayName.isNotEmpty ? displayName : query,
      );
    } catch (e) { debugPrint('錯誤：$e'); continue; }
  }
  return null;
}

Future<List<_RoutePoint>?> _analyzeRoute(
    List<_Station> waypoints, DateTime departure, double speedKmh) async {
  final results = <_RoutePoint>[];
  double totalKm = 0;
  DateTime eta = departure;

  for (int i = 0; i < waypoints.length; i++) {
    if (i > 0) {
      final dist = _haversineKm(
        waypoints[i - 1].lat, waypoints[i - 1].lng,
        waypoints[i].lat,     waypoints[i].lng,
      );
      totalKm += dist;
      eta = eta.add(Duration(minutes: (dist / speedKmh * 60).round()));
    }
    final w = await _fetchHourAt(waypoints[i].lat, waypoints[i].lng, eta);
    if (w == null) return null;
    results.add(_RoutePoint(
      station: waypoints[i], distFromStart: totalKm,
      arrivalTime: eta, weather: w,
    ));
  }
  return results;
}

