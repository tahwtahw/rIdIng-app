  Widget _buildTownBody() {
    // 計算是否全部仍在載入
    final totalTownships = _kRegions.fold(0, (s, r) => s + r.townships.length);
    final loaded = _townWeatherMap.length;
    final isInitialLoad = loaded == 0 && _townLoadingAll;

    if (isInitialLoad) {
      return const Center(child: Column(
        mainAxisSize: MainAxisSize.min,
        children: [
          CircularProgressIndicator(),
          SizedBox(height: 12),
