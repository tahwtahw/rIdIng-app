/// 從台灣道路行政區資料庫查詢途經鄉鎮，有座標的才加入結果。
/// 支援輸入格式：台12乙線、台12乙、12乙線、12乙、12、121、121線 等。
List<_Township> _lookupRoadTownships(String query) {
  final q = query.trim().replaceAll('臺', '台').replaceAll(' ', '');
  if (q.isEmpty) return [];

  // ── 取出數字＋地支部分 ──────────────────────────────────────
  final numSuffix = RegExp(r'\d+[甲乙丙丁戊己庚]?').firstMatch(q)?.group(0) ?? '';
  final hasTai    = q.startsWith('台');

  // ── 候選鍵值列表（依優先順序嘗試）────────────────────────────
  final candidates = <String>{};
  candidates.add(q);
  if (!q.endsWith('線')) candidates.add('${q}線');
  if (!hasTai && numSuffix.isNotEmpty) {
    candidates.add('台${numSuffix}線');
    candidates.add('台$numSuffix');
  }

  List<String>? districts;
  for (final c in candidates) {
    final hit = _kRoadDistricts[c];
    if (hit != null && hit.isNotEmpty) { districts = hit; break; }
  }

  // ── 數字模糊比對（"12" 找所有以12為數字的鍵）─────────────────
  if (districts == null && numSuffix.isNotEmpty) {
    for (final key in _kRoadDistricts.keys) {
      final kNum = RegExp(r'\d+[甲乙丙丁戊己庚]?').firstMatch(key)?.group(0) ?? '';
      if (kNum == numSuffix) {
        districts = _kRoadDistricts[key];
        break;
      }
    }
  }

  if (districts == null || districts.isEmpty) return [];

  // ── 比對 _kAllTownships ────────────────────────────────────
  // 檔案地名常無「區/鎮/鄉/市」後綴，需要容錯比對
  bool _matches(_Township t, String name) {
    if (t.name == name) return true;
    // 去掉後綴後比對
    final stripped = t.name.replaceAll(RegExp(r'[區鎮鄉市]$'), '');
    return stripped == name;
  }

  final result = <_Township>[];
  for (final entry in districts) {
    final parts  = entry.split('|');
    final name   = parts[0];
    final county = parts.length > 1 ? parts[1] : '';
    final countyCore = county.replaceAll(RegExp(r'[市縣]$'), ''); // 去掉市/縣

    _Township? found;

    if (county.isNotEmpty) {
      // 有縣市提示 → 嚴格比對縣市
      try {
        found = _kAllTownships.firstWhere(
          (t) => _matches(t, name) && t.county.contains(countyCore),
        );
      } catch (e) { debugPrint('錯誤：$e'); }
    }

    if (found == null) {
      // 無縣市提示 → 只在名稱唯一時採用（防止歧義）
      final all = _kAllTownships.where((t) => _matches(t, name)).toList();
      if (all.length == 1) found = all.first;
      // 多個結果＆無縣市提示 → 跳過，避免抓到錯誤地區
    }

    if (found != null &&
        !result.any((t) => t.name == found!.name && t.county == found!.county)) {
      result.add(found);
    }
  }
  return result;
}


