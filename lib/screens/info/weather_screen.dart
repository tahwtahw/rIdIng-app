  // 非同步抓取所有鄉鎮天氣（依序，避免同時發出太多請求）
  Future<void> _fetchAllTownWeather() async {
    setState(() => _townLoadingAll = true);
    for (final region in _kRegions) {
      for (final t in region.townships) {
        final pw = await _fetchPointWeather(t.lat, t.lng, t.name);
        if (!mounted) return;
        setState(() => _townWeatherMap[t.name] = pw);
      }
    }
    if (mounted) setState(() => _townLoadingAll = false);
  }

  void _switchDay(int i) => setState(() => _selectedDay = i);

  // 自由搜尋
  Future<void> _doFreeSearch(String keyword) async {
    if (keyword.trim().isEmpty) return;
    final kw = keyword.trim();
    setState(() { _roadState = _RoadState.loading; _errorMsg = null; });

    if (_looksLikeRoad(kw)) {
      // ── 1. 道路行政區資料庫（255條省道＋縣市道）──────────────
      final dbTownships = _lookupRoadTownships(kw);
      if (dbTownships.length >= 2) {
        setState(() {
          _roadState = _RoadState.townshipPicker;
          _foundRoadName = _roadDisplayName(kw);
          _foundRoadPts = const [];
          _foundRoadTownships = dbTownships;
        });
        return;
      }

      // ── 2. 預設路段精確站點（_kRoads）────────────────────────
      // 支援輸入：台7甲、7甲、7甲線、台7甲線、丁/戊/己/庚 也算
      final qNum = kw.replaceAll(RegExp(r'[^0-9甲乙丙丁戊己庚]'), '');
      if (qNum.isNotEmpty) {
        final presetKey = _kRoads.keys.firstWhere(
          (k) => k.replaceAll(RegExp(r'[^0-9甲乙丙丁戊己庚]'), '') == qNum,
          orElse: () => '',
        );
        if (presetKey.isNotEmpty) {
          setState(() => _roadState = _RoadState.idle);
          _showRouteSetup(presetKey, _kRoads[presetKey]!);
          return;
        }
      }

      // ── 3. 找不到 → 顯示全台鄉鎮選擇器（可搜尋），不做 geocoding ──
      setState(() {
        _roadState = _RoadState.townshipPicker;
        _foundRoadName = _roadDisplayName(kw);
        _foundRoadPts = const [];
        _foundRoadTownships = _kAllTownships;
      });
      return;
    }

    // ── 一般地名搜尋（非路段）────────────────────────────────
    final geo = await _geocode(kw);
    if (!mounted) return;
    if (geo == null) {
      setState(() {
        _roadState = _RoadState.error;
        _errorMsg = '找不到「$kw」的位置資訊。\n\n建議輸入方式：\n'
            '• 路段號碼，例如「台64線」、「縣道122」\n'
            '• 路段＋地名，例如「台3線 苗栗」\n'
            '• 縣市鄉鎮名稱，例如「花蓮縣」、「仁愛鄉」';
      });
      return;
    }
    final pw = await _fetchPointWeather(geo.lat, geo.lng, geo.name);
    if (!mounted) return;
    if (pw == null) {
      setState(() { _roadState = _RoadState.error; _errorMsg = '無法取得天氣資料，請稍後再試。'; });
    } else {
      setState(() { _roadState = _RoadState.point; _pointWeather = pw; });
    }
  }

  Future<void> _doCoordSearch(_Station s) async {
    setState(() { _roadState = _RoadState.loading; _errorMsg = null; });
    _searchCtrl.text = s.name;
    final pw = await _fetchPointWeather(s.lat, s.lng, s.name);
    if (!mounted) return;
    if (pw == null) {
      setState(() { _roadState = _RoadState.error; _errorMsg = '無法取得天氣資料，請稍後再試。'; });
    } else {
      setState(() { _roadState = _RoadState.point; _pointWeather = pw; });
    }
  }

  Future<void> _doRouteAnalysis(String roadName, List<_Station> stations,
      int startIdx, int endIdx, DateTime departure, double speed) async {
    setState(() { _roadState = _RoadState.loading; _routeTitle = null; });
    final waypoints = stations.sublist(startIdx, endIdx + 1);
    final results = await _analyzeRoute(waypoints, departure, speed);
    if (!mounted) return;
    if (results == null) {
      setState(() { _roadState = _RoadState.error; _errorMsg = '無法取得部分站點天氣，請稍後再試。'; });
    } else {
      setState(() {
        _roadState   = _RoadState.route;
        _routePoints = results;
        _routeTitle  = '$roadName ${stations[startIdx].name}→${stations[endIdx].name}';
      });
    }
  }

  // ── 常用路段 ────────────────────────────────────────────────
  Future<void> _loadFavorites() async {
    final prefs = await SharedPreferences.getInstance();
    if (!mounted) return;
