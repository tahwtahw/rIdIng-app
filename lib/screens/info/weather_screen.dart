class _RouteSetupSheet extends StatefulWidget {
  final String roadName;
  final List<_Station> stations;
  final double defaultSpeed;
  final void Function(int startIdx, int endIdx,
      DateTime departure, double speed) onAnalyze;
  const _RouteSetupSheet({required this.roadName, required this.stations,
      required this.defaultSpeed, required this.onAnalyze});
  @override State<_RouteSetupSheet> createState() => _RouteSetupSheetState();
}

class _RouteSetupSheetState extends State<_RouteSetupSheet> {
  late int _startIdx;
  late int _endIdx;
  late TextEditingController _speedCtrl;
  DateTime _departure = DateTime.now();
  String?  _speedError;

  @override
  void initState() {
    super.initState();
    _startIdx = 0;
    _endIdx   = widget.stations.length - 1;
    _speedCtrl = TextEditingController(
        text: widget.defaultSpeed.toStringAsFixed(0));
  }

  @override
  void dispose() { _speedCtrl.dispose(); super.dispose(); }

  double get _estKm {
    double km = 0;
    for (int i = _startIdx; i < _endIdx; i++) {
      km += _haversineKm(
        widget.stations[i].lat,   widget.stations[i].lng,
        widget.stations[i+1].lat, widget.stations[i+1].lng,
      );
    }
    return km;
  }

  double? get _parsedSpeed {
    final v = double.tryParse(_speedCtrl.text.trim());
    if (v == null || v <= 0 || v > 100) return null;
    return v;
  }

  Duration get _estTime {
    final s = _parsedSpeed ?? widget.defaultSpeed;
    return Duration(minutes: (_estKm / s * 60).round());
  }

  void _submit() {
    final speed = _parsedSpeed;
    if (speed == null) {
      setState(() => _speedError = '請輸入 1～100 之間的數值');
      return;
    }
    widget.onAnalyze(_startIdx, _endIdx, _departure, speed);
  }

  @override
  Widget build(BuildContext context) {
    final cs  = Theme.of(context).colorScheme;
    final est = _estTime;
    final h   = est.inHours;
    final m   = est.inMinutes % 60;

    return Padding(
      padding: EdgeInsets.only(
          bottom: MediaQuery.of(context).viewInsets.bottom),
      child: SingleChildScrollView(
        child: Column(mainAxisSize: MainAxisSize.min, children: [
          const SizedBox(height: 8),
          Container(width: 36, height: 4,
              decoration: BoxDecoration(color: Colors.grey.shade400,
                  borderRadius: BorderRadius.circular(2))),
          Padding(
            padding: const EdgeInsets.fromLTRB(16, 12, 16, 4),
            child: Text(widget.roadName,
                style: const TextStyle(fontWeight: FontWeight.bold, fontSize: 16)),
          ),
          const Divider(),

          Padding(
            padding: const EdgeInsets.symmetric(horizontal: 16, vertical: 6),
            child: Row(children: [
              const Icon(Icons.trip_origin, size: 18, color: Colors.green),
              const SizedBox(width: 8),
              const Text('起點', style: TextStyle(fontWeight: FontWeight.w600)),
              const Spacer(),
              DropdownButton<int>(
                value: _startIdx,
                items: List.generate(widget.stations.length, (i) =>
                    DropdownMenuItem(value: i,
                        child: Text(widget.stations[i].name))),
                onChanged: (v) {
                  if (v != null) setState(() {
                    _startIdx = v;
                    if (_endIdx <= _startIdx) _endIdx = _startIdx + 1;
                  });
                },
              ),
            ]),
          ),

          Padding(
            padding: const EdgeInsets.symmetric(horizontal: 16, vertical: 6),
            child: Row(children: [
              const Icon(Icons.place, size: 18, color: Colors.red),
              const SizedBox(width: 8),
              const Text('終點', style: TextStyle(fontWeight: FontWeight.w600)),
              const Spacer(),
              DropdownButton<int>(
                value: _endIdx,
                items: List.generate(widget.stations.length, (i) =>
                    DropdownMenuItem(
                      value: i,
                      enabled: i > _startIdx,
                      child: Text(widget.stations[i].name,
                          style: TextStyle(
                              color: i > _startIdx ? null : Colors.grey)),
                    )),
                onChanged: (v) {
                  if (v != null && v > _startIdx) setState(() => _endIdx = v);
                },
              ),
            ]),
          ),

          Padding(
            padding: const EdgeInsets.fromLTRB(16, 0, 16, 8),
            child: Row(children: [
              Icon(Icons.straighten, size: 13, color: cs.outline),
              const SizedBox(width: 4),
              Text('${_estKm.toStringAsFixed(1)} km',
                  style: TextStyle(fontSize: 12, color: cs.outline)),
              const SizedBox(width: 12),
              Icon(Icons.schedule, size: 13, color: cs.outline),
              const SizedBox(width: 4),
              Text('預計 $h 時 $m 分',
                  style: TextStyle(fontSize: 12, color: cs.outline)),
            ]),
          ),
          const Divider(),

          Padding(
            padding: const EdgeInsets.symmetric(horizontal: 16, vertical: 8),
            child: Row(children: [
              const Icon(Icons.schedule, size: 18),
              const SizedBox(width: 8),
              const Text('出發時間', style: TextStyle(fontWeight: FontWeight.w600)),
              const Spacer(),
              OutlinedButton(
                style: OutlinedButton.styleFrom(
                    padding: const EdgeInsets.symmetric(
                        horizontal: 16, vertical: 10)),
                onPressed: () async {
                  final now = DateTime.now();
                  final picked = await showTimePicker(
                    context: context,
                    initialTime: TimeOfDay.fromDateTime(_departure),
                    helpText: '選擇出發時間',
                  );
                  if (picked != null) {
                    setState(() => _departure = DateTime(
                        now.year, now.month, now.day,
                        picked.hour, picked.minute));
                  }
                },
                child: Text(_fmtTime(_departure),
                    style: const TextStyle(
                        fontSize: 18, fontWeight: FontWeight.bold,
                        letterSpacing: 1)),
              ),
            ]),
          ),
          const Divider(),

          Padding(
            padding: const EdgeInsets.fromLTRB(16, 8, 16, 4),
            child: Row(crossAxisAlignment: CrossAxisAlignment.start, children: [
              const Padding(
                padding: EdgeInsets.only(top: 12),
                child: Icon(Icons.speed, size: 18),
              ),
              const SizedBox(width: 8),
              const Padding(
                padding: EdgeInsets.only(top: 14),
                child: Text('平均速度',
                    style: TextStyle(fontWeight: FontWeight.w600)),
              ),
              const Spacer(),
              SizedBox(
                width: 110,
                child: TextField(
                  controller: _speedCtrl,
                  keyboardType: const TextInputType.numberWithOptions(decimal: true),
                  textAlign: TextAlign.center,
                  style: const TextStyle(fontSize: 18, fontWeight: FontWeight.bold),
                  decoration: InputDecoration(
                    suffixText: 'km/h',
                    errorText: _speedError,
                    isDense: true,
                    contentPadding: const EdgeInsets.symmetric(
                        horizontal: 8, vertical: 10),
                    border: const OutlineInputBorder(),
                  ),
                  onChanged: (_) => setState(() => _speedError = null),
                ),
              ),
            ]),
          ),
          Padding(
            padding: const EdgeInsets.fromLTRB(16, 4, 16, 8),
            child: Text('參考：山地騎乘約 10~13，一般路段約 15~20，公路車約 22~30',
                style: TextStyle(fontSize: 11, color: cs.outline)),
          ),
          const Divider(),

          Padding(
            padding: const EdgeInsets.fromLTRB(16, 8, 16, 16),
            child: FilledButton.icon(
              onPressed: _submit,
              icon: const Icon(Icons.analytics),
              label: const Text('開始分析路線天氣'),
              style: FilledButton.styleFrom(
                  minimumSize: const Size.fromHeight(46)),
            ),
          ),
        ]),
      ),
    );
  }
}

