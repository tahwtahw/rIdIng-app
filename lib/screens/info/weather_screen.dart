class _TownshipCard extends StatelessWidget {
  final String          name;
  final _PointWeather?  weather;
  final int             dayIndex;
  final VoidCallback    onTap;

  const _TownshipCard({
    required this.name, required this.weather,
    required this.dayIndex, required this.onTap,
  });

  @override
  Widget build(BuildContext context) {
    if (weather == null) {
      // 還在載入
      return Container(
        width: 88,
        margin: const EdgeInsets.only(right: 8, bottom: 8),
        padding: const EdgeInsets.all(10),
        decoration: BoxDecoration(
          color: Theme.of(context).colorScheme.surfaceVariant,
          borderRadius: BorderRadius.circular(12),
        ),
        child: Column(mainAxisAlignment: MainAxisAlignment.center, children: [
          Text(name, style: const TextStyle(fontSize: 12, fontWeight: FontWeight.bold)),
          const SizedBox(height: 8),
          const SizedBox(
            width: 20, height: 20,
            child: CircularProgressIndicator(strokeWidth: 2),
          ),
        ]),
      );
    }

    // 取指定天的資料
    final int code;
    final double tempH, tempL;
    final int precip;
    final double wind;

    if (dayIndex == 0) {
      code   = weather!.current.weatherCode;
      tempH  = weather!.daily.isNotEmpty ? weather!.daily[0].tempMax : weather!.current.temp;
      tempL  = weather!.daily.isNotEmpty ? weather!.daily[0].tempMin : weather!.current.temp;
      precip = weather!.daily.isNotEmpty ? weather!.daily[0].precipProb : 0;
      wind   = weather!.current.windSpeed;
    } else {
      final di = dayIndex < weather!.daily.length ? dayIndex : weather!.daily.length - 1;
      final d  = weather!.daily[di];
      code   = d.weatherCode;
      tempH  = d.tempMax;
      tempL  = d.tempMin;
      precip = d.precipProb;
      wind   = d.windMax;
    }

    final color = _weatherColor(code);
    return GestureDetector(
      onTap: onTap,
      child: Container(
        width: 88,
        margin: const EdgeInsets.only(right: 8, bottom: 8),
        padding: const EdgeInsets.symmetric(vertical: 10, horizontal: 8),
        decoration: BoxDecoration(
          color: color.withOpacity(0.08),
          borderRadius: BorderRadius.circular(12),
          border: Border.all(color: color.withOpacity(0.28)),
        ),
        child: Column(mainAxisSize: MainAxisSize.min, children: [
          Text(name,
              style: const TextStyle(fontSize: 12, fontWeight: FontWeight.bold),
              overflow: TextOverflow.ellipsis),
          const SizedBox(height: 6),
          Icon(_weatherIcon(code), color: color, size: 26),
          const SizedBox(height: 3),
          Text(_weatherLabel(code),
              style: TextStyle(fontSize: 9, color: color),
              overflow: TextOverflow.ellipsis),
          const SizedBox(height: 4),
          Text('${tempH.round()}° / ${tempL.round()}°',
              style: const TextStyle(fontSize: 11, fontWeight: FontWeight.w600)),
          const SizedBox(height: 3),
          Row(mainAxisAlignment: MainAxisAlignment.center, children: [
            const Icon(Icons.water_drop, size: 9, color: Color(0xFF1565C0)),
            const SizedBox(width: 2),
            Text('$precip%',
                style: const TextStyle(fontSize: 9, color: Color(0xFF1565C0))),
          ]),
          Row(mainAxisAlignment: MainAxisAlignment.center, children: [
            const Icon(Icons.air, size: 9, color: Color(0xFF00695C)),
            const SizedBox(width: 2),
            Text('${wind.toStringAsFixed(0)} km/h',
                style: const TextStyle(fontSize: 9, color: Color(0xFF00695C))),
          ]),
          const SizedBox(height: 3),
          Text('點擊查看詳情',
              style: TextStyle(fontSize: 8,
                  color: Theme.of(context).colorScheme.outline)),
        ]),
      ),
    );
  }
}

