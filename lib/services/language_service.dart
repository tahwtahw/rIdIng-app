  static String get _customKey =>
      'custom_langs_${AuthService.currentUser?.id ?? 'device'}';

  static Future<void> load() async {
    final p = await SharedPreferences.getInstance();
    // 未設定過語言時，預設使用裝置系統語言
    notifier.value = p.getString(_kLang) ?? _deviceDefault();
    customLangs = p.getStringList(_customKey) ?? [];
    AuthService.notifier.addListener(_reloadCustom);
  }

  /// 依裝置語系挑選預設語言；無對應翻譯時回退英文
  static String _deviceDefault() {
    final locale = ui.PlatformDispatcher.instance.locale;
    final lang = locale.languageCode.toLowerCase();
    if (lang == 'zh') {
      final script = (locale.scriptCode ?? '').toLowerCase();
      final country = (locale.countryCode ?? '').toUpperCase();
      if (script == 'hans' || country == 'CN' || country == 'SG') {
        return 'zh-CN';
      }
      return 'zh-TW';
    }
    if (presets.containsKey(lang)) return lang;
    return 'en';
  }

  static Future<void> _reloadCustom() async {
    final p = await SharedPreferences.getInstance();
    customLangs = p.getStringList(_customKey) ?? [];
  }

  static Future<void> setLang(String code) async {
    final p = await SharedPreferences.getInstance();
    await p.setString(_kLang, code);
    notifier.value = code;
  }

  static Future<bool> addCustomLang(String name) async {
    final n = name.trim();
    if (n.isEmpty || customLangs.contains(n) || presets.containsValue(n)) {
      return false;
    }
    customLangs.add(n);
    final p = await SharedPreferences.getInstance();
    await p.setStringList(_customKey, customLangs);
    return true;
  }

  static Future<void> removeCustomLang(String name) async {
    customLangs.remove(name);
    final p = await SharedPreferences.getInstance();
    await p.setStringList(_customKey, customLangs);
    if (notifier.value == 'custom:$name') {
      await setLang('zh-TW');
    }
  }

  static String displayName(String code) {
    if (code.startsWith('custom:')) return code.substring(7);
    return presets[code] ?? code;
  }

  /// 取得目前語言的翻譯字串；自訂語言與缺漏字串回退繁中
  static String t(String key) {
    final lang = notifier.value;
    return _tr[lang]?[key] ??
        _tr2[lang]?[key] ??
        _tr['zh-TW']![key] ??
        _tr2['zh-TW']![key] ??
        key;
  }

  /// 帶參數的翻譯，例如 tp('msg_joined', {'x': title})
  /// 字串中以 {x}、{n} 等佔位
  static String tp(String key, Map<String, String> params) {
    var s = t(key);
    params.forEach((k, v) => s = s.replaceAll('{$k}', v));
    return s;
  }

